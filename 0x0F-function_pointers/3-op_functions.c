#include "3-calc.h"

/**
  * op_add - func
  * @a: a
  * @b: b
  * Return: add
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - func
  * @a: a
  * @b: b
  * Return: sub
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - func
  * @a: first
  * @b: funct
  * Return: mul
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - func
  * @a: first
  * @b: second
  * Return: div
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - fun
  * @a: 1onc
  * @b: 2nd
  * Return: mod
  *
  */
int op_mod(int a, int b)
{
	return (a % b);
}
